def print_first_10_natural_numbers_for():
    print("Printing the first 10 natural numbers using a for loop:")
    for i in range(1, 11):
        print(i, end=" ")

def main():
    print_first_10_natural_numbers_for()

if __name__ == "__main__":
    main()
def print_first_10_natural_numbers_while():
    print("\n\nPrinting the first 10 natural numbers using a while loop:")
    i = 1
    while i <= 10:
        print(i, end=" ")
        i += 1

def main():
    print_first_10_natural_numbers_while()

if __name__ == "__main__":
    main()
